var mongoose = require('mongoose');

//this is similar to creating a class model in Dart
var Todo = mongoose.model('Todo', {
    text: {
        type: String,
        required: true,
        minlength: 1,
        trim: true
    },
    completed: {
        type: Boolean,
        default: false
    },
    completedAt: {
        type: Number,
        default: null
    }
}, 'Todos');

module.exports = { Todo };